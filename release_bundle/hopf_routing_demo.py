def _wrap_to_pi(x: np.ndarray) -> np.ndarray:
    """Wrap angles to [-π, π]."""
    return (x + np.pi) % (2.0 * np.pi) - np.pi


def hopf_transport_sectors(
    Z: np.ndarray,
    K: int,
    dims: Tuple[int, int, int, int] = DIMS,
    lam: float = LAMBDA,
) -> np.ndarray:
    """
    Assign each token to a Hopf angular sector.

    Parameters
    ----------
    Z : (N, D) float array, L2-normalized row vectors
    K : number of routing sectors
    dims : 4-tuple of dimension indices (dim_i, dim_j, dim_k, dim_l)
    lam : phase transport strength (1.0 = canonical TRANS mode)

    Returns
    -------
    sectors : (N,) int array of sector assignments in [0, K)

    Algorithm
    ---------
    1. Project to 4D subspace z = (a, b, c, d) = Z[:, dims]
    2. Hopf base coordinates:
         chi_u = rho2² / (rho1² + rho2²)         [0..1]
         delta = arctan2(b,a) - arctan2(d,c)      [−π..π]
         alpha = 0.5 × (arctan2(b,a) + arctan2(d,c))  [−π..π]
    3. Phase transport:
         transported_alpha = alpha + (λ/2)·cos(2χ)·delta
    4. Assign to integer bins (chi, delta, alpha) with product ≤ K
    """
    a = Z[:, dims[0]]
    b = Z[:, dims[1]]
    c = Z[:, dims[2]]
    d = Z[:, dims[3]]

    rho1 = np.sqrt(a * a + b * b)
    rho2 = np.sqrt(c * c + d * d)
    denom = np.maximum(np.sqrt(rho1 ** 2 + rho2 ** 2), 1e-12)

    chi_u = np.clip((rho2 / denom) ** 2, 0.0, 1.0 - 1e-12)  # sin²(χ)
    chi = np.arcsin(np.clip(rho2 / denom, 0.0, 1.0))

    theta1 = np.arctan2(b, a)
    theta2 = np.arctan2(d, c)
    delta = _wrap_to_pi(theta1 - theta2)
    alpha = _wrap_to_pi(0.5 * (theta1 + theta2))

    # Levi-Civita connection correction (INC-0147: mechanism clarification)
    transported_alpha = _wrap_to_pi(alpha + 0.5 * lam * np.cos(2.0 * chi) * delta)

    kchi, kdelta, kalpha = _allocate_bins(K)

    bchi   = np.minimum((chi_u * kchi).astype(np.int64), kchi - 1)
    bdelta = np.minimum(((delta  + np.pi) / (2.0 * np.pi) * kdelta).astype(np.int64),
                        kdelta - 1)
    balpha = np.minimum(((transported_alpha + np.pi) / (2.0 * np.pi) * kalpha).astype(np.int64),
                        kalpha - 1)

    return (bchi * kdelta * kalpha + bdelta * kalpha + balpha).astype(np.int64)


def _allocate_bins(K: int, min_b: int = 2) -> Tuple[int, int, int]:
    """
    Find (k1, k2, k3) with k1×k2×k3 ≤ K, product maximized, spread minimized.
    Direct port of allocate_triplet_bins_budget from hyperbolic_router_so8.py.
    """
    K = max(1, int(K))
    best: Tuple[int, int, int] = (1, K, 1)
    best_score = None
    for k1 in range(min_b, K + 1):
        for k2 in range(min_b, K + 1):
            max_k3 = K // max(k1 * k2, 1)
            if max_k3 < min_b:
                break
            for k3 in range(min_b, max_k3 + 1):
                prod = k1 * k2 * k3
                favor = 1 if k2 >= k3 else 0
                spread = abs(k1 - k2) + abs(k2 - k3) + abs(k1 - k3)
                score = (prod, favor, -spread, k2, -k3)
                if best_score is None or score > best_score:
                    best_score = score
                    best = (k1, k2, k3)
    return best


