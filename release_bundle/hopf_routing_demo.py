def effective_bucket_count(sectors: np.ndarray) -> float:
    """exp(H(p)) — perplexity of the sector visit distribution."""
    counts = np.array(list(Counter(sectors.tolist()).values()), dtype=np.float64)
    p = counts / counts.sum()
    return float(np.exp(-np.sum(p * np.log(p + 1e-30))))


def gini_coefficient(sectors: np.ndarray) -> float:
    """Gini coefficient of sector visit counts (0=uniform, 1=all in one)."""
    counts = np.sort(np.array(list(Counter(sectors.tolist()).values()),
                              dtype=np.float64))
    n = len(counts)
    if n <= 1:
        return 0.0
    idx = np.arange(1, n + 1, dtype=np.float64)
    return float(2.0 * np.sum(idx * counts) / (n * counts.sum()) - (n + 1) / n)


def fit_power_law(k_vals: List[int], eff_vals: List[float]) -> Tuple[float, float, float]:
    """OLS fit of log(eff) = alpha*log(K) + log(c). Returns (alpha, c, R²)."""
    log_k = np.log(np.array(k_vals, dtype=np.float64))
    log_e = np.log(np.array(eff_vals, dtype=np.float64))
    A = np.vstack([log_k, np.ones(len(log_k))]).T
    coeffs, *_ = np.linalg.lstsq(A, log_e, rcond=None)
    alpha, log_c = float(coeffs[0]), float(coeffs[1])
    c = float(np.exp(log_c))
    pred = alpha * log_k + log_c
    ss_res = float(np.sum((log_e - pred) ** 2))
    ss_tot = float(np.sum((log_e - log_e.mean()) ** 2))
    r2 = float(1.0 - ss_res / max(ss_tot, 1e-30))
    return alpha, c, r2


